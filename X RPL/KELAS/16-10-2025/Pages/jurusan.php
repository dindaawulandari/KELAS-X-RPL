<h2 class="text-danger mb-4">Program Keahlian di SMKN 2 Buduran</h2>

<p>
SMKN 2 Buduran memiliki enam program keahlian unggulan yang dirancang untuk menyiapkan peserta didik agar siap menghadapi dunia kerja, 
melanjutkan pendidikan, maupun berwirausaha. Setiap jurusan didukung dengan fasilitas praktik lengkap, guru berpengalaman, 
serta kurikulum berbasis industri.
</p>

<h5 class="mt-4 text-primary">1. Rekayasa Perangkat Lunak (RPL)</h5>
<p>
Jurusan RPL mempelajari cara merancang, membuat, dan mengembangkan perangkat lunak seperti aplikasi, website, maupun sistem informasi. 
Siswa diajarkan bahasa pemrograman, desain antarmuka, basis data, serta analisis kebutuhan pengguna. 
Lulusan RPL banyak bekerja sebagai programmer, web developer, dan software engineer di berbagai perusahaan teknologi, 
atau bahkan menjadi wirausaha digital yang menciptakan produk sendiri.
</p>

<h5 class="mt-4 text-primary">2. Akuntansi (AK)</h5>
<p>
Jurusan Akuntansi melatih siswa agar mahir dalam mencatat, mengelola, dan melaporkan keuangan dengan teliti dan jujur. 
Mata pelajaran yang diajarkan meliputi akuntansi perusahaan jasa dan dagang, perpajakan, audit, hingga komputer akuntansi menggunakan software MYOB dan Excel. 
Lulusan AK memiliki peluang besar bekerja di kantor akuntan, perusahaan, instansi pemerintahan, maupun menjadi konsultan keuangan.
</p>

<h5 class="mt-4 text-primary">3. Bisnis Daring (BD)</h5>
<p>
Jurusan BD membekali siswa dengan keterampilan berbisnis di era digital. 
Siswa diajarkan membuat toko online, mengelola media sosial bisnis, fotografi produk, dan strategi digital marketing. 
Dengan kemampuan promosi online, desain konten, serta manajemen pelanggan, lulusan BD dapat bekerja di bidang pemasaran, periklanan, atau menjadi pengusaha muda.
</p>

<h5 class="mt-4 text-primary">4. Layanan Perbankan (LPB)</h5>
<p>
Layanan Perbankan merupakan jurusan yang berfokus pada pelayanan keuangan dan administrasi perbankan. 
Siswa dilatih menjadi tenaga frontliner profesional, seperti teller dan customer service, dengan pembelajaran simulasi bank. 
Selain teori perbankan, siswa juga mendapat pelatihan komunikasi, pelayanan pelanggan, dan etika kerja. 
Jurusan ini bekerja sama dengan beberapa bank di Sidoarjo untuk praktik kerja lapangan (PKL).
</p>

<h5 class="mt-4 text-primary">5. Desain Komunikasi Visual (DKV)</h5>
<p>
DKV adalah jurusan seni dan teknologi yang mempelajari bagaimana menyampaikan pesan melalui media visual. 
Siswa belajar fotografi, ilustrasi, tipografi, desain grafis, dan editing video menggunakan software profesional seperti Adobe Photoshop, Illustrator, dan Premiere Pro. 
Lulusan DKV banyak bekerja sebagai desainer grafis, ilustrator, fotografer, dan kreator konten di dunia industri kreatif.
</p>

<h5 class="mt-4 text-primary">6. Manajemen Perkantoran (MP)</h5>
<p>
Jurusan MP mempersiapkan siswa menjadi tenaga administrasi yang terampil, disiplin, dan profesional. 
Siswa mempelajari pengarsipan dokumen, pelayanan tamu, pengetikan surat resmi, serta pengelolaan jadwal pimpinan. 
Dengan keterampilan komunikasi dan pengelolaan informasi, lulusan MP siap bekerja di instansi pemerintah, perusahaan swasta, atau membuka usaha jasa administrasi sendiri.
</p>

<p class="mt-4">
Seluruh jurusan di SMKN 2 Buduran terus berinovasi dan beradaptasi dengan perkembangan dunia industri. 
Melalui kerja sama dengan dunia usaha dan dunia industri, sekolah memastikan lulusannya siap bersaing di dunia kerja modern.
</p>