<h2 class="text-danger mb-4">Kegiatan SMKN 2 BUDURAN</h2>
<p>
SMKN 2 Buduran rutin mengadakan berbagai kegiatan yang bertujuan membentuk karakter dan mengembangkan potensi siswa. 
Kegiatan dilakukan secara bergilir setiap semester agar siswa tidak hanya fokus belajar di kelas, tetapi juga berkembang secara sosial dan emosional.
</p>

<ul>
    <li>MPLS (Masa Pengenalan Lingkungan Sekolah) – kegiatan awal tahun ajaran untuk memperkenalkan budaya sekolah kepada siswa baru.</li>
    <li>Classmeeting – ajang adu bakat olahraga dan seni antarkelas yang penuh semangat kebersamaan.</li>
    <li>Kunjungan Industri – kegiatan siswa ke perusahaan untuk belajar langsung dunia kerja.</li>
    <li>Ekstrakurikuler – seperti Pramuka, PMR, Paskibra, Voli, dan Futsal.</li>
</ul>